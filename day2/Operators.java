package com.chainsys.day2;
public class Operators {

	public static void main(String[] args) {
		
		int num1 = 30; int num2 = 70;
		
		int Add = num1 + num2;
		int Sub = num1 - num2;
		int Mul = num1 * num2;
		int Div = num1 / num2;
		int Mod = num1 % num2;
		
		System.out.println("Add : " + Add + "\n"+"Sub : " + Sub + "\n"+"Mul : " + Mul + "\n"+"Div : " + Div + "\n"+"Mod : " + Mod );
	}
}
